#function without argument
fname = function()
{
  
  print("hello")
}

fname()
#function with argument without return value
fname = function(a,b)
{
  c=a+b
  print(c)
}

fname(2,3)

#function with argument with return value

fname = function(a,b)
{
  c=a+b
  return(c)
}

fname(2,3)





